# Write a Program to calculate total weight of 3 Persons & average weight of 3 Person.
p1 = int(input("Enter the weight of person1: "))
p2 = int(input("Enter the weight of person2: "))
p3 = int(input("Enter the weight of person3: "))
t_weight = p1+p2+p3
avg_weight = t_weight/3
print("The total weight of 3 persons: ",t_weight)
print("The average weight of 3 persons: ",avg_weight)